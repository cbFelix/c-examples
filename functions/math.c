//      Basic Mathematical Operations

float add(float a, float b) {
        return a + b;
}
float sub(float a, float b) {
        return a - b;
}
float mul(float a, float b) {
        return a * b;
}
float devide(float a, float b) {
        return a / b;
}

int main() {
        int a = 3;
        int b = 2;

        add(a, b);
        sub(a, b);
        mul(a, b);
        devide(a, b);

        return 0;
}